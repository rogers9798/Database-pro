<script>

function cycleBackgrounds() {
    var index = 0;
 
    $imageEls = $('.contain .slide'); 
 
    setInterval(function () {
        
        index = index + 1 < $imageEls.length ? index + 1 : 0;
        
        // Show the next
        $imageEls.eq(index).addClass('show');
        
        // Hide the previous
        $imageEls.eq(index - 1).removeClass('show');
    }, 6000);
}
 

$(function () {
    cycleBackgrounds();
});

